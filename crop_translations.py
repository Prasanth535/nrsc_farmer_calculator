def get_translated_text(text, lang_code):
    """
    Helper function to safely look up translations.
    If exact text not found, returns the original text (English).
    It is case-insensitive for lookup to be robust.
    """
    if not text or lang_code == 'en':
        return text
    
    text_str = str(text).strip()
    
    # 1. Try exact match
    if text_str in CROP_TRANSLATIONS:
        return CROP_TRANSLATIONS[text_str].get(lang_code, text_str)
    
    # 2. Try case-insensitive match (robustness)
    lower_map = {k.lower(): k for k in CROP_TRANSLATIONS.keys()}
    if text_str.lower() in lower_map:
        original_key = lower_map[text_str.lower()]
        return CROP_TRANSLATIONS[original_key].get(lang_code, text_str)
        
    # 3. Fallback: Return original English text
    return text_str